package com.card.demo.domain;

public class DataInterface {

	private Long id;
	private String name;
	private String dtInterface;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDtInterface() {
		return dtInterface;
	}
	public void setDtInterface(String dtInterface) {
		this.dtInterface = dtInterface;
	}
	
	
}
